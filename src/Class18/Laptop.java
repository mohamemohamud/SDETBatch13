package Class18;

public class Laptop {
    String make;
    String model;
    String storage;
    String memory;
    double screenSize;



    public Laptop(String make, String model, String storage, String memory, double screenSize) {
        this.make = make;
        this.model = model;
        this.storage = storage;
        this.memory = memory;
        this.screenSize = screenSize;
    }
}
